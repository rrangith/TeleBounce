	/***Constructor***/
	Map(int level) {
		map = allMaps[level-1];		//find the selected map using the current level
		//loop through all elements/tiles in the map array
		for (int j = 0; j < map.length; j++) {
			for (int i = 0; i < map[1].length;i++) {
				if (map[j][i] == 5) {	//if this element is the telepad
					telepadBound = new Rectangle(i*width, j*height, width, height);		//get this telepad's bound info
				} else if (map[j][i] == 2) {	//if this element is a wall
					walls.add(new Rectangle(i*width, j*height, width, height));		//get this wall's bound info and add it to the list
				} else if (map[j][i] == 0) {	//if this element is ground
					int random = (int)Math.round(Math.random()*100);	//generate a random number out of 100
					if (random == 1) {	//if this random number is 1
						items.add(new Item(i*width, j*height, "health"));	//add a health potion with this tile info to the list of items
					} else if (random == 50) {	//if this random number is 50
						items.add(new Item(i*width, j*height, "speed"));	//add a speed potion with this tile info to the list of items
					}
				}
			}
		}
	}
	
	/** getItems
	 * returns the list of items generated in the map constructor
	 * @return items, ArrayList<Item>: the list of items and its tile position
	 */
	public ArrayList<Item> getItems() {
		return items;
	}
	
	/** paint
	 * void method that paints all tiles and info on screen/frame, based on the map array
	 * @param g Graphics2D: painting tool
	 */
	public void paint(Graphics2D g) {
		//loop through all elements/tiles in the map array
		for (int j = 0; j < map.length; j++) {
			for (int i = 0; i < map[1].length;i++) {
				g.drawImage(ground.getImage(), i*width, j*height, width, height, null);		//draw ground regardless of the element
				if (map[j][i] == 5) {	//if this is a telepad tile
					g.drawImage(telepad.getImage(), i*width, j*height, width, height, null);	//draw telepad on this position
				}else if (map[j][i] == 1) {		//if this is a stone ground tile
					g.drawImage(stoneGround.getImage(), i*width, j*height, width, height, null);	//draw stone ground on this position
				}else if (map[j][i] == 2) {		//if this is a wall tile
					g.drawImage(wall.getImage(), i*width, j*height, width, height, null);	//draw wall on this position
				}
			}
		}
	}
	
	/** blocked
	 * check if the passed bound(Rectangle) intersects with any of the wall tile's bound
	 * @param bound, Rectangle: the bound to be checked
	 * @return boolean: if this bound collides with any of the wall tile's bound
	 */
	public boolean blocked(Rectangle bound) {
		for (Rectangle wall: walls) {	//loop through the wall tile list
			if (wall.intersects(bound)) {	//if this wall tile intersects the given bound
				return true;	//return true
			}
		}
		return false;	//if none of them intersected the bound, return false
	}
	
	/** checkCountDown
	 * check if the passed bound collides with the telepad tile's bound
	 * @param playerBound, Rectangle: the bound to be checked
	 * @return boolean: if this bound intersects the telepad tile's bound
	 */
	public boolean checkCountDown(Rectangle playerBound) {		
		return playerBound.intersects(telepadBound);
	}
